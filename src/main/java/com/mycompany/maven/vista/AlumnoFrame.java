    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if (fila >= 0) {
            int resp = JOptionPane.showConfirmDialog(null, "Realmente desea Modificar al Alumno...!");
            if (JOptionPane.OK_OPTION == resp) {
                int ida = Integer.parseInt(tbalumnos.getValueAt(fila, 0).toString());
                Alumno al = new Alumno();
                al.setIdalumno(Integer.parseInt(tbalumnos.getValueAt(fila, 0).toString()));
                al.setNombres(txtnombres.getText());
                al.setApellidos(txtapellidos.getText());
                al.setDireccion(txtdireccion.getText());
                al.setCorreo(txtcorreo.getText());
                al.setIdescuela(escuelaDao.read2(cboEscuela.getSelectedItem().toString()).getIdescuela());
                int op = alumnDao.update(al);
                if (op == 1) {
                    JOptionPane.showMessageDialog(rootPane, "Registro Modificado correctamente...!");
                    limpiartabla();
                    desabilitar();
                    listarAlumnos();
                    
                } else {
                    JOptionPane.showMessageDialog(rootPane, "No se pudo Modificado el Alumno...!");
                }
            } else {
                System.out.println("No selecciona una opci贸n afirmativa");
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Seleccionar Alumno...!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

