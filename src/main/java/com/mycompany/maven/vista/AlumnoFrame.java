    public void listarEscuela() {
        model = (DefaultComboBoxModel) cboEscuela.getModel();
        List<Escuela> lista = escuelaDao.readAll();
        cboEscuela.addItem("Seleccionar Escuela...");
        for (int i = 0; i < lista.size(); i++) {
            model.addElement(lista.get(i).getNombre());
        }
        cboEscuela.setModel(model);
    }

    public void listarAlumnos() {
        dtm = (DefaultTableModel) tbalumnos.getModel();
        List<Map<String, Object>> lista1 = alumnDao.lista();
        Object[] datos = new Object[5];
        lista1.forEach(map -> {
            datos[0] = map.get("idalumno");
            datos[1] = map.get("nombres") + " " + map.get("apellidos");
            datos[2] = map.get("direccion");
            datos[3] = map.get("correo");
            datos[4] = map.get("escuela");
            dtm.addRow(datos);
        });
        tbalumnos.setModel(dtm);
    }

    public void desabilitar() {
        cboEscuela.setSelectedIndex(0);
        txtnombres.setText(null);
        txtapellidos.setText(null);
        txtdireccion.setText(null);
        txtcorreo.setText(null);
        txtnombres.setEnabled(false);
        txtapellidos.setEnabled(false);
        txtdireccion.setEnabled(false);
        txtcorreo.setEnabled(false);
    }

    public void limpiartabla() {
        for (int i = 0; i < tbalumnos.getRowCount(); i++) {
            dtm.removeRow(i);
            i -= 1;
        }
    }

